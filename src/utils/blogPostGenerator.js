// Blog Post Generator Function
export const generateBlogPosts = async (statusCallback) => {
    try {
        statusCallback('Initializing blog post generation...');
        
        // First, create enhanced categories
        statusCallback('Creating enhanced categories...');
        ENHANCED_CATEGORIES.forEach(category => {
            try {
                const existingCategories = BLOG_CONFIG.getCategories();
                const categoryExists = existingCategories.find(cat => cat.slug === category.slug);
                if (!categoryExists) {
                    BLOG_CONFIG.createCategory(category);
                }
            } catch (error) {
                console.error(`Error creating category ${category.name}:`, error);
            }
        });
        
        // Create enhanced tags
        statusCallback('Creating comprehensive tag system...');
        ENHANCED_TAGS.forEach(tag => {
            try {
                const existingTags = BLOG_CONFIG.getTags();
                const tagExists = existingTags.find(t => t.slug === tag.slug);
                if (!tagExists) {
                    BLOG_CONFIG.createTag(tag);
                }
            } catch (error) {
                console.error(`Error creating tag ${tag.name}:`, error);
            }
        });
        
        // Generate all blog posts
        const allPosts = [...BLOG_POSTS_DATA, ...generateAdditionalPosts()];
        
        statusCallback(`Generating ${allPosts.length} unique blog posts...`);
        
        let successCount = 0;
        for (let i = 0; i < allPosts.length; i++) {
            const postData = allPosts[i];
            
            try {
                const post = {
                    ...postData,
                    date: new Date(Date.now() - (i * 24 * 60 * 60 * 1000)).toISOString(),
                    views: Math.floor(Math.random() * 500) + 50,
                    featured: i < 5 // Make first 5 posts featured
                };
                
                BLOG_CONFIG.createPost(post);
                successCount++;
                
                if ((successCount) % 10 === 0) {
                    statusCallback(`Generated ${successCount} of ${allPosts.length} posts...`);
                }
            } catch (error) {
                console.error(`Error creating post ${postData.title}:`, error);
            }
        }
        
        statusCallback(`Successfully generated ${successCount} blog posts!`);
        return successCount;
        
    } catch (error) {
        console.error('Error generating blog posts:', error);
        throw new Error(`Failed to generate blog posts: ${error.message}`);
    }
};